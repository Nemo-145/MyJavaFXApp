package com.example.myjavafxapp.models;

public class GeneralSigns {
    // fields
    private int age;
    private String dogShows;
    private String fullname;

    // methods
    public int getAge() {
        return age;
    }

    public void setAge(int newVal) {
        this.age = newVal;
    }

    public String getDogShows() {
        return dogShows;
    }

    public void setDogShows(String newVal) {
        this.dogShows = newVal;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String newVal) {
        this.fullname = newVal;
    }
}
